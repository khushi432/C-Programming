/*
    // 4. / Division

*/
#include<stdio.h>
int main()
{
int num = 25;
int num1 = 5;
printf("num/num1: %d\n",num/num1); // 25/5
printf("num/num1: %d\n",num1/num); // 5/25

float kuku = 25;
float kuku1 = 5;
printf("kuku/kuku1: %.2f\n",kuku/kuku1); // 25/5
printf("kuku1/kuku: %.2f\n",kuku1/kuku); // 5/25

double ne = 6.3;
double ne1 = 3.3;
printf("ne/ne1: %f\n", ne/ne1);
printf("ne1/ne: %f\n", ne1/ne);

}