#include<stdio.h>
int main()
{
int num = 5;
int num1 = 6;
printf("num*num1: %d\n", num*num1);
printf("num1*num: %d\n", num1*num);

float nu = 5.2;
float nu1 = 5.3;
printf("nu*nu1: %f\n", nu*nu1);
printf("nu1*nu: %f\n", nu1*nu);

double ne = 6.2;
double ne1 = 6.3;
printf("ne*ne1: %f\n", ne*ne1);
printf("ne1*ne: %f\n", ne1*ne);


}