#include <stdio.h>

int main() {
  // +
  int a = 10;
  int b = 20;
  printf("a+b: %d\n", a+b);

  // -
  int c = 20;
  int d = 10;
  printf("a-b: %d\n", c-d);

  // *
  int e = 5;
  int f = 4;
  printf("a*b: %d\n", e*f);

  // /
  int g = 20;
  int h = 4;
  printf("a/b: %d\n", g/h);

  // %
  int i = 21;
  int j = 5;
  printf("a mod b: %d\n", i%j);




 
 
}